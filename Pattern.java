import java.util.*;
import java.lang.*;
import java.io.*;

class pattern
{
	public static void main (String[] args) throws java.lang.Exception
	{
		int N= 6;
		for(int i = 1 ; i <= N ; i++){
		    for(int j = N - i + 1 ; j >= 1 ; j--){
		        System.out.print(j );
		    }
		    System.out.println(" ");
		}
		

	}
}

